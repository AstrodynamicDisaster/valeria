"""
ValerIA Database Reset Script
Drops all tables and recreates a clean database for testing

WARNING: This will delete ALL data in the database!
"""

import os
import sys
from sqlalchemy import create_engine, text, MetaData
from sqlalchemy.exc import OperationalError
from setup_database import Base, create_database_engine


def confirm_reset():
    """Ask user to confirm database reset"""
    print("âš ï¸  WARNING: This will DELETE ALL data in the ValerIA database!")
    print("   This action cannot be undone.")
    print()

    response = input("Are you sure you want to reset the database? (type 'YES' to confirm): ")

    if response.strip() != 'YES':
        print("âŒ Database reset cancelled.")
        return False

    return True


def reset_database():
    """Drop all tables and reset database to clean state"""

    if not confirm_reset():
        return False

    print("ğŸ”„ Starting database reset...")

    try:
        # Create database engine
        engine = create_database_engine()

        print("âœ“ Database connection established")

        # Drop all tables using SQLAlchemy metadata
        print("ğŸ—‘ï¸  Dropping all existing tables...")

        # Reflect current database structure
        metadata = MetaData()
        metadata.reflect(bind=engine)

        # Drop all tables in reverse dependency order
        metadata.drop_all(engine)

        print("âœ“ All tables dropped successfully")

        # Optionally, you can also clean up any sequences, functions, etc.
        with engine.connect() as conn:
            # Drop any remaining sequences (auto-generated by SERIAL columns)
            result = conn.execute(text("""
                SELECT sequence_name FROM information_schema.sequences
                WHERE sequence_schema = 'public';
            """))

            sequences = result.fetchall()
            for (seq_name,) in sequences:
                try:
                    conn.execute(text(f"DROP SEQUENCE IF EXISTS {seq_name} CASCADE;"))
                    print(f"  âœ“ Dropped sequence: {seq_name}")
                except Exception as e:
                    print(f"  âš ï¸  Could not drop sequence {seq_name}: {e}")

            # Commit the changes
            conn.commit()

        print("âœ“ Database reset completed successfully!")
        print()
        print("ğŸ’¡ You can now run: python setup_database.py")

        return True

    except OperationalError as e:
        if "does not exist" in str(e).lower():
            print("âŒ Database connection failed. Make sure PostgreSQL is running.")
        else:
            print(f"âŒ Database error: {e}")
        return False

    except Exception as e:
        print(f"âŒ Unexpected error during reset: {e}")
        return False


def reset_documents_folder():
    """Reset the local documents folder"""
    print("ğŸ—‘ï¸  Resetting documents folder...")

    import shutil

    docs_dir = "./documents"

    if os.path.exists(docs_dir):
        try:
            shutil.rmtree(docs_dir)
            print("âœ“ Documents folder removed")
        except Exception as e:
            print(f"âš ï¸  Could not remove documents folder: {e}")

    # Recreate empty documents folder
    os.makedirs(docs_dir, exist_ok=True)
    print("âœ“ Clean documents folder created")


